function day_itostr(x){
    switch(x){
        case 1:
            return("Lundi");
            break;
        case 2:
            return("Mardi");
            break;
        case 3:
            return("Mercredi");
            break;
        case 4:
            return("Jeudi");
            break;
        case 5:
            return("Vendredi");
            break;
        case 6:
            return("Samedi");
            break;
        case 0:
            return("Dimanche");
            break;
    }
}

function month_itostr(x){
    switch(x){
        case 0:
            return ("Janvier");
            break;
        case 1:
            return ("Février");
            break;
        case 2:
            return ("Mars");
            break;
        case 3:
            return ("Avril");
            break;
        case 4:
            return ("Mai");
            break;
        case 5:
            return ("Juin");
            break;
        case 6:
            return ("Juillet");
            break;
        case 7:
            return ("Août");
            break;
        case 8:
            return ("Septembre");
            break;
        case 9:
            return ("Octobre");
            break;
        case 10:
            return ("Novembre");
            break;
        case 11:
            return ("Décembre");
            break;
    }
}

